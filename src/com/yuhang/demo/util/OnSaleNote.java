package com.yuhang.demo.util;

public class OnSaleNote {

	private long userId;
	private String opt;
	private long[] shopItems;

	public long getUserId() {
		return userId;
	}

	public void setUserId(long userId) {
		this.userId = userId;
	}

	public String getOpt() {
		return opt;
	}

	public void setOpt(String opt) {
		this.opt = opt;
	}

	public long[] getShopItems() {
		return shopItems;
	}

	public void setShopItems(long[] shopItems) {
		this.shopItems = shopItems;
	}

}
